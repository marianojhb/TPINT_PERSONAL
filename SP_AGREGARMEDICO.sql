CREATE OR ALTER PROCEDURE SP_AGREGARMEDICO

 @DNI NCHAR(9),
 @NOMBRE VARCHAR(50),
 @APELLIDO VARCHAR(50),
 @TIPO NCHAR(2),
 @SEXO CHAR,
 @NACIONALIDAD VARCHAR(50),
 @FECHANAC DATE,
 @DIRECCION VARCHAR(50),
 @IDPROVINCIA INT,
 @IDLOCALIDAD INT,
 @EMAIL VARCHAR(50),
 @TELEFONO VARCHAR(50),
-- LEGAJO SE CARGA SOLO
 @CODESPECIALIDAD INT,
 @HORARIO VARCHAR(100),
-- ESTADO SE CARGA SOLO
 @USUARIO VARCHAR(20),
 @PASSWORD VARCHAR(20)
 
 AS

BEGIN

INSERT INTO USUARIOS(
	dni_U,
	nombre_U,
	apellido_U,
	tipo_U,
	sexo_U,
	nacionalidad_U,
	fechaNac_U,
	direccion_U,
	idLocalidad_U,
	idProvincia_U,
	email_U,
	telefono_U,
	codEspecialidad_U,
	horario_U,
	usuario_U,
	password_U)
VALUES (
	 @DNI,
	 @NOMBRE,
	 @APELLIDO,
	 @TIPO,
	 @SEXO,
	 @NACIONALIDAD,
	 @FECHANAC,
	 @DIRECCION,
	 @IDPROVINCIA,
	 @IDLOCALIDAD,
	 @EMAIL,
	 @TELEFONO,
	 @CODESPECIALIDAD,
	 @HORARIO,
	 @USUARIO,
	 @PASSWORD
)
END
GO
